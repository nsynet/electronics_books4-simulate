//-----------------------------------------------------------------
// 关闭IAP
//-----------------------------------------------------------------
void IapIdle() {
	IAP_CONTR = 0;					//关闭IAP功能
	IAP_CMD = 0;					//清除命令寄存器
	IAP_TRIG = 0;					//清除触发寄存器
	IAP_ADDRH = 0x80;				//将地址设置到非IAP区域
	IAP_ADDRL = 0;
}
//-----------------------------------------------------------------
// 从指定的ISP/IAP/EEPROM区域地址读取一字节
//-----------------------------------------------------------------
u8 IapReadByte(u16 addr) {
	u8 dat;							//数据缓冲区
	IAP_CONTR = ENABLE_IAP;			//使能IAP
	IAP_CMD = CMD_READ;				//设置IAP命令（读字节命令）
	IAP_ADDRL = addr;				//设置IAP低8位地址
	IAP_ADDRH = addr >> 8;			//设置IAP高8位地址
	IAP_TRIG = 0x5A;				//写触发命令(0x5A)
	IAP_TRIG = 0xA5;				//写触发命令(0xA5)
	_nop_();						//等待ISP/IAP/EEPROM操作完成
	dat = IAP_DATA;					//读ISP/IAP/EEPROM数据
	IapIdle();						//关闭IAP功能
	return dat;						//返回读取的数据字节
}
//-----------------------------------------------------------------
// 向指定的ISP/IAP/EEPROM区域地址写入一字节
//-----------------------------------------------------------------
void IapProgramByte(u16 addr, u8 dat) {
	IAP_CONTR = ENABLE_IAP;			//使能IAP
	IAP_CMD = CMD_PROGRAM;			//设置IAP命令（写字节命令：编程）
	IAP_ADDRL = addr;				//设置IAP低8地址
	IAP_ADDRH = addr >> 8;			//设置IAP高8地址
	IAP_DATA = dat;					//写ISP/IAP/EEPROM数据
	IAP_TRIG = 0x5A;				//写触发命令(0x5A)
	IAP_TRIG = 0xA5;				//写触发命令(0xA5)
	_nop_();						//等待ISP/IAP/EEPROM操作完成
	IapIdle();
}
//-----------------------------------------------------------------
// 扇区擦除
//-----------------------------------------------------------------
void IapEraseSector(u16 addr) {
	IAP_CONTR = ENABLE_IAP;			//使能IAP
	IAP_CMD = CMD_ERASE;			//设置IAP命令（删除扇区命令）
	IAP_ADDRL = addr;				//设置IAP低地址
	IAP_ADDRH = addr >> 8;			//设置IAP高地址
	IAP_TRIG = 0x5A;				//写触发命令(0x5A)
	IAP_TRIG = 0xA5;				//写触发命令(0xA5)
	_nop_();						//等待ISP/IAP/EEPROM操作完成
	IapIdle();
}
