//-----------------------------------------------------------------
// 延时函数
//-----------------------------------------------------------------
void Delay100us()		//@12.000MHz
{
	u8 i = 2, j = 39;
	do {
		while (--j);
	} while (--i);
}
//-----------------------------------------------------------------
void Delay1ms()		//@12.000MHz
{
	u8 i = 12, j = 169;
	do {
		while (--j);
	} while (--i);
}
//-----------------------------------------------------------------
void delay_ms(u8 x) {
	while(x--) Delay1ms();
}
