#define MAIN_Fosc 12000000L			//时钟频率定义
sbit LED = P2^0;					//LED连接在P2.0引脚
//-----------------------------------------------------------------
// 延时函数(参数取值限于1~255)
//-----------------------------------------------------------------
void delay_ms(u8 ms) {
	u16 i;
	do{
		i = MAIN_Fosc / 13000;
		while(--i);
	}while(--ms);
}
