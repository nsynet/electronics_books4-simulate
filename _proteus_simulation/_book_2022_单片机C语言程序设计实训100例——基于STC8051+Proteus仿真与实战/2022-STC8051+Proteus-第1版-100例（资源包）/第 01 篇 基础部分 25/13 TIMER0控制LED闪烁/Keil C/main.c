#include "STC15xxx.h"
#include <intrins.h>
#define u8 	unsigned char
#define u16	unsigned int
//-----------------------------------------------------------------
// T0初始化(16位自动重装载模式/12分频,5ms@12MHz)
//-----------------------------------------------------------------
void Timer0Init() {
	AUXR &= 0x7F;		//定时器时钟12T模式
	TMOD &= 0xF0;		//设置定时器模式
	TL0 = 0x78;			//设置定时初值
	TH0 = 0xEC;			//设置定时初值
	//上述两行由STC-ISP自动生成的语句还可写成以下语句
	//TL0 = (65536 - 5000) & 0x00FF;
	//TH0 = (65536 - 5000) >> 8;
	//或者：
	//TL0 = - 5000 & 0x00FF;
	//TH0 = - 5000 >> 8;
	TF0 = 0;				//清除TF0标志
	TR0 = 1;				//定时器0开始计时
}
