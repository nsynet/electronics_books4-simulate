//------------------------- delay.c -------------------------------
// 名称: 延时函数
//-----------------------------------------------------------------
#include	"delay.h"
//-----------------------------------------------------------------
// 延时函数（参数取值：1~65535,自动适应主时钟）
//-----------------------------------------------------------------
void delay_ms(u16 ms) {
	u16 i;
	do{
		i = (u16)(MAIN_Fosc / 13000);
		while(--i);
	}while(--ms);
}