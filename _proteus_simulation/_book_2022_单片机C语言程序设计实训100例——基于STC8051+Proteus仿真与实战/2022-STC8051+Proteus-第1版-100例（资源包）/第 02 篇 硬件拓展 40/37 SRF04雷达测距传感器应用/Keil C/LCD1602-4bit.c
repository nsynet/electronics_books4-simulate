void Delay120us()	{	//@12.000MHz
	unsigned char i, j;
	i = 2; 	j = 99;
	do
	{
		while (--j);
	} while (--i);
}
void Delay1ms()		//@12.000MHz
{
	u8 i = 12, j = 169;
	do {
		while (--j);
	} while (--i);
}
//-----------------------------------------------------------------
// 延时函数
//-----------------------------------------------------------------
void delay_ms(u8 x) {
	while(x--) Delay1ms();
}
