// 延时（120us,1ms,xms）
//-----------------------------------------------------------------
void Delay120us(){		//@12MHz
	u8 i = 2, j = 99;
	do {
		while (--j);
	} while (--i);
}
//-----------------------------------------------------------------
void Delay1ms() {			//@12MHz
	u8 i = 12, j = 169;
	do {
		while (--j);
	} while (--i);
}
//-----------------------------------------------------------------
void delay_ms(u8 x) {
	while(x--) Delay1ms();
}
