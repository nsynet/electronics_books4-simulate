//-----------------------------------------------------------------
// 延时（1ms,xms）
//-----------------------------------------------------------------
void Delay1ms() {		//@12MHz
	u8 i = 12, j = 169;
	do
	{
		while (--j);
	} while (--i);
}
//-----------------------------------------------------------------
void delay_ms(u8 x) {
	while(x--) Delay1ms();
}
