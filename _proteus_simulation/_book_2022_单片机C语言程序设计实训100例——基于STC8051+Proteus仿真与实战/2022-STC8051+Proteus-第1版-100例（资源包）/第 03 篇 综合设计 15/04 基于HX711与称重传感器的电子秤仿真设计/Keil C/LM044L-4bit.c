//-----------------------------------------------------------------
// 延时函数:1ms,xms @12.000MHz
//-----------------------------------------------------------------
void Delay1ms() {
	u8 i = 12, j = 169;
	do {
		while (--j);
	} while (--i);
}
//-----------------------------------------------------------------
void delay_ms(u8 x) {
	while(x--) Delay1ms();
}
