/**
  * @brief  向GPIO写入命令
  * @param  usCmd :要写入的命令（表寄存器地址）
  * @retval 无
  */	
 void GPIO_Write_Cmd ( uint8_t usCmd )
{
	GPIO_LCD_CS_CLR;//开始片选      
	GPIO_LCD_DC_CLR;//写命令
	GPIO_LCD_RD_SET;//禁止读
	DATAOUT(usCmd);//输出命令
	GPIO_LCD_WR_CLR;//写入开始
	//GPIO_Delay(10);
	GPIO_LCD_WR_SET;//写入结束
	GPIO_LCD_CS_SET;//结束片选        	
}


/**
  * @brief  向GPIO写入数据
  * @param  usData :要写入的数据
  * @retval 无
  */	
 void GPIO_Write_Data ( uint16_t usData )
{
	GPIO_LCD_CS_CLR;//开始片选      
	GPIO_LCD_DC_SET;//写数据
	GPIO_LCD_RD_SET;//禁止读
	DATAOUT(usData);//输出数据
	GPIO_LCD_WR_CLR;//写入开始
	//GPIO_Delay(5);
	GPIO_LCD_WR_SET;//写入结束
	GPIO_LCD_CS_SET;//结束片选   
	
}


/**
  * @brief  从GPIO读取数据
  * @param  无
  * @retval 读取到的数据
  */	
 uint16_t GPIO_Read_Data ( void )
{
	uint16_t data;
	
#if	 	 GPIO_DATA_USE_ONEPORT == 1	
	
 	GPIO_DATA_PORT->CRL=0X88888888; //上拉输入
	GPIO_DATA_PORT->CRH=0X88888888; //上拉输入
	GPIO_DATA_PORT->ODR=0X0000;     //全部输出0

#elif  GPIO_DATA_USE_ANYPORT == 1
	GPIO_SET_DATA_OUT(GPIO_Set_Mode_IN);
	DATAOUT(0X0000);
#endif		
	
	GPIO_LCD_DC_SET;
	GPIO_LCD_WR_SET;

	GPIO_LCD_CS_CLR;
	//读取数据
	GPIO_LCD_RD_CLR;    
    
	data = DATAIN();  
	GPIO_LCD_RD_SET;
	GPIO_LCD_CS_SET; 

#if	 	 GPIO_DATA_USE_ONEPORT == 1	
	
	GPIO_DATA_PORT->CRL=0X33333333; // 上拉输出
	GPIO_DATA_PORT->CRH=0X33333333; // 上拉输出
	GPIO_DATA_PORT->ODR=0XFFFF;    //全部输出高

#elif  GPIO_DATA_USE_ANYPORT == 1
	GPIO_SET_DATA_OUT(GPIO_Set_Mode_OUT);
	DATAOUT(0XFFFF);
#endif	
	return data;  
}


