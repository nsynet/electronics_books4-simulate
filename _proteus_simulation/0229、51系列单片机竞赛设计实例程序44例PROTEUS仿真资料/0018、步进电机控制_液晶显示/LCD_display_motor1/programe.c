//**************LCD初始化 *************** 
//*********************************
void iniLCD(void)                               //初始化 
{ L=1;R=1;
  wcode(0x38);
  wcode(0x0f);	                                //开显示 设置 
  wcode(0xc0);	                                  //设置显示启动为第一行 
  wcode(0x01);	                                    //清屏
  wcode(0x06);	                                 	//画面不动，光标右移。
}
//******************LCD判断忙的子程序**********
//**********************************************8
 void chkbusy(void)	                         //   测LCD忙状态 
{ 
  E=1;				                               //使能LCD
  RS=0;				                               //读写指令 
  RW=1;				                                  //读 
  P2=0xff;			                               //读操作前先进行一次空读操作 ，接下来才能读到数据 
  while(!Busy);		                                 //等待，不忙退出 
 }
 //*****************************************
 //*****************写指令代码*****************
//***********************************************
void wcode(uc cd)	                                //写指令 代码 
{ 
  chkbusy();					                     //写等待 
  P2=0xff;						                	  //使能LCD
  RW=0;						                    	 //读禁止 
  RS=0;						                     	 //输出 设置 
  P2=cd;					                    	 //写数据代码 
  E=1;						                      	 //以下两句产生下降沿 
  E=0;                                                 // 
}
//*******************把显示数据写到内存单元中 *****************
void wdata(uc dat)	                                   //写显示数据
{
  chkbusy();					                        //写等待 
  P2=0xff;							                     //使能LCD
  RW=0;							                           //读禁止 
  RS=1;						                         	 //输出 设置 
  P2=dat;						                        //写数据代码 
  E=1;							                         //以下两句产生下降沿 
  E=0;                                                         // 
 }
